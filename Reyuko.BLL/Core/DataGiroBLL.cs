using Reyuko.BLL.Interface;
using Reyuko.DAL;
using Reyuko.DAL.Domain;
using Reyuko.Utils;
using Reyuko.Utils.Common;
using Reyuko.Utils.Error;
using System;

namespace Reyuko.BLL.Core
{
    public class DataGiroBLL : BaseBLL, IDataGiroBLL
    {
        private string methodName = "";
        private int traceID = 0;

        public int AddDataGiro(DataGiro oData)
        {
            methodName = "AddDataGiro";
            traceID = 1;

            using (var uow = new UnitOfWork(AppConfig.Current.ContextName))
            {
                using (var trans = uow.BeginTransaction())
                {
                    try
                    {
                        traceID = 2;
                        DataGiro oNewDataGiro = new DataGiro();
                        oNewDataGiro.MapFrom(oData);
                        oNewDataGiro = uow.DataGiro.Add(oNewDataGiro);
                        uow.Save();

                        traceID = 3;
                        oData.Id = oNewDataGiro.Id;
                        trans.Commit();
                    }
                    catch (Exception ex)
                    {
                        trans.Rollback();
                        throw new AppException(500, methodName, traceID, ex);
                    }
                }
            }

            return oData.Id;
        }

        public bool EditDataGiro(DataGiro oData)
        {
            methodName = "EditDataGiro";
            traceID = 1;

            using (var uow = new UnitOfWork(AppConfig.Current.ContextName))
            {
                traceID = 2;
                var oDBData = uow.DataGiro.Get(oData.Id);
                if (oDBData != null)
                {
                    using (var trans = uow.BeginTransaction())
                    {
                        try
                        {
                            traceID = 3;
                            oDBData.MapFrom(oData);
                            uow.DataGiro.Update(oDBData);
                            uow.Save();

                            traceID = 4;
                            trans.Commit();
                        }
                        catch (Exception ex)
                        {
                            trans.Rollback();
                            throw new AppException(500, methodName, traceID, ex);
                        }
                    }
                }
            }

            return true;
        }

        public bool RemoveDataGiro(int id)
        {
            methodName = "RemoveDataGiro";
            traceID = 1;

            using (var uow = new UnitOfWork(AppConfig.Current.ContextName))
            {
                using (var trans = uow.BeginTransaction())
                {
                    try
                    {
                        traceID = 2;
                        DataGiro oDBDataGiro = uow.DataGiro.SingleOrDefault(m => m.Id == id);
                        if (oDBDataGiro != null)
                        {
                            traceID = 3;
                            uow.DataGiro.Remove(id);
                            uow.Save();
                        }

                        traceID = 5;
                        trans.Commit();
                    }
                    catch (Exception ex)
                    {
                        trans.Rollback();
                        throw new AppException(500, methodName, traceID, ex);
                    }
                }
            }

            return true;
        }


    }
}
